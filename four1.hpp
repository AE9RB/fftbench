// Cooley-Tukey algorithm based on "Numerical Recipes in C+" p.513
// Cambridge University Press, 2002
// by W.H. Press, S.A. Teukolsky, W.T. Vetterling, and B.P. Flannery

// The only change is a template allowing float/double selection.

#ifndef fftbench_four1_hpp
#define fftbench_four1_hpp

template<typename T>
void four1(T* data, unsigned long nn) {
    unsigned long n, mmax, m, j, istep, i;
    T wtemp, wr, wpr, wpi, wi, theta;
    T tempr, tempi;
    // reverse-binary reindexing
    n = nn<<1;
    j=1;
    for (i=1; i<n; i+=2) {
        if (j>i) {
            std::swap(data[j-1], data[i-1]);
            std::swap(data[j], data[i]);
        }
        m = nn;
        while (m>=2 && j>m) {
            j -= m;
            m >>= 1;
        }
        j += m;
    };
    // Danielson-Lanczos
    mmax=2;
    while (n>mmax) {
        istep = mmax<<1;
        theta = -(2*M_PI/mmax);
        wtemp = sin(0.5*theta);
        wpr = -2.0*wtemp*wtemp;
        wpi = sin(theta);
        wr = 1.0;
        wi = 0.0;
        for (m=1; m < mmax; m += 2) {
            for (i=m; i <= n; i += istep) {
                j=i+mmax;
                tempr = wr*data[j-1] - wi*data[j];
                tempi = wr * data[j] + wi*data[j-1];
                data[j-1] = data[i-1] - tempr;
                data[j] = data[i] - tempi;
                data[i-1] += tempr;
                data[i] += tempi;
            }
            wtemp=wr;
            wr += wr*wpr - wi*wpi;
            wi += wi*wpr + wtemp*wpi;
        }
        mmax=istep;
    }
}

#endif
